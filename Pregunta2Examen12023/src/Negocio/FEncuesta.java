    public int Cambio(JRadioButton y) {
        if (y.isSelected()) {
            return 1;
        }
        return 0;
    }
    private void Limpiar() {
      jTextField1.setText("");
      jTextField2.setText("");
      jTextField3.setText("");
      jComboBox1.setSelectedIndex(0);
      jRadioButton1.setSelected(false);
      
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre = jTextField1.getText();
        int dia = jComboBox1.getSelectedIndex() + 1;
        int mes = jComboBox2.getSelectedIndex() + 1;
        int anio = Integer.parseInt(jComboBox3.getSelectedItem().toString());

        int nroHijos = jComboBox4.getSelectedIndex() + 1;
        String estadoCivilString = jComboBox5.getSelectedItem().toString();
        switch (estadoCivilString) {

            case "Soltero":
                estadoCivil = 0;
                break;
            case "Divorsiado":
                estadoCivil = 1;
                break;
            case "Casado":
                estadoCivil = 2;
                break;
            case "Viudo":
                estadoCivil = 3;
                break;
        }

        int primaria = Cambio(jRadioButton1);
        int bachiller = Cambio(jRadioButton2);
        int tecnico = Cambio(jRadioButton3);
        int licenciado = Cambio(jRadioButton4);
        int postgrado = Cambio(jRadioButton5);
        int ci = Integer.parseInt(jTextField2.getText());
        int telefono = Integer.parseInt(jTextField3.getText());
        int hora = jComboBox6.getSelectedIndex() + 1 - 2;
        int minuto = jComboBox7.getSelectedIndex() + 1 - 2;
        int segundo = jComboBox8.getSelectedIndex() + 1 - 2;
        A.insertar(nombre, dia, mes, anio, nroHijos, estadoCivil,
                primaria, bachiller, tecnico, licenciado, postgrado,
                ci, telefono, hora, minuto, segundo);
        jTextArea1.setText(A.mostrar(pos));
        jLabel9.setText(String.valueOf(A.getCantidad()));
        //
        Limpiar();

    }//GEN-LAST:event_jButton1ActionPerformed
