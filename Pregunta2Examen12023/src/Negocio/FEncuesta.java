    private boolean SoloLetras(String Cadena, JTextField jTextField1) {
        if (Cadena.matches("[a-zA-Z][a-zA-Z ]*")) {
            return true;
        }else{
            return false;
        }
        
    }
    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        if (jTextField1.getText().length() != 0) {
            if (!SoloLetras(jTextField1.getText(), jTextField1)) {
                JOptionPane.showMessageDialog(rootPane, "Dato Invalido", "TDA Encuesta", HEIGHT);
                jTextField1.setText("");
            }
        }
    }//GEN-LAST:event_jTextField1KeyReleased

